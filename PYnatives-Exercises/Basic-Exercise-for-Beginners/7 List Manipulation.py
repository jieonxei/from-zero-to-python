# Create a list of 5 fruits. Add a new fruit to the end of the list, then remove the second fruit (at index 1).

fruits = ["apple", "banana", "cherry", "strawberry", "blueberry"]

# to add use the "append"
fruits.append("mango")

# to remove use "pop"
fruits.pop(1)

print(fruits)